let isPress = false

function randSong() {
    const albumImg = document.getElementById("albumImg");
    const links = document.getElementById("links");
    const spoty = document.getElementById("spoty");
    const yt = document.getElementById("yt");
    const apple = document.getElementById("apple");
    const deezer = document.getElementById("deezer");
    const cover = document.getElementById("cover");
    const songName = document.getElementById("songName");
    
    if (!isPress) {
        albumImg.src = "https://www.covermesongs.com/wp-content/uploads/2016/09/Weezer_Pinkerton.jpg";
        albumImg.style.boxShadow = "0 4px 8px 0 rgba(255, 255, 255, 0.2), 0 6px 20px 0 rgba(255, 255, 255, 0.19)";
        links.style.display = "initial";
        isPress = true;
    }
    let num = Math.floor(Math.random()*19);
    songName.innerHTML = '~   ' + jsongs.songs[num].name + '   ~';
    spoty.href = jsongs.songs[num].spoty;
    yt.href = jsongs.songs[num].ytmus;
    apple.href = jsongs.songs[num].apple;
    deezer.href = jsongs.songs[num].deezr;
    cover.href = jsongs.songs[num].genius;
}
